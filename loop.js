//incremental for loop
const numbers = [1, 22, 33, 2, 5, 55, 100];

let sum = 0;
for (let i = 0; i <= numbers.length; i++) {
  // console.log(i);
  sum += i;
}
// console.log(sum);

//decremental for loop

for (let i = 20; i > numbers.length; i--) {
  // console.log(i);
}

// incremental while loop

let i = 0;
while (i < numbers.length) {
  // console.log(i);
  i++;
}

//decremental while loop
let x = 20;
while (x >= numbers.length) {
  // console.log(x);
  x--;
}

//for of

for (number of numbers) {
  console.log(number);
}
